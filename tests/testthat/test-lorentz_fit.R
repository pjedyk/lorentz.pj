})

test_that("lorentz_fit() auto vs manual init converge", {
  set.seed(100)
  x_test <- seq(-10, 10, length.out = 200)
  y_test <- .lorentz_fn(x_test, f0 = 2, gamma = 3, a = 5)
  y_noisy <- y_test + rnorm(length(y_test), sd = 0.2)
  fit <- lorentz_fit(x_test, y_noisy)
  fit_manual <- lorentz_fit(x_test, y_noisy, f0_init = 2, gamma_init = 3, a_init = 5)

  expect_equal(fit$f0, fit_manual$f0, tolerance = 1e-4)
  expect_equal(fit$gamma, fit_manual$gamma, tolerance = 1e-4)
  expect_equal(fit$a, fit_manual$a, tolerance = 1e-4)
})
